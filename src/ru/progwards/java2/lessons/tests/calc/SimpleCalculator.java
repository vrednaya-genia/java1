package ru.progwards.java2.lessons.tests.calc;

public class SimpleCalculator {

    int sum(int val1, int val2) {

        return val1+val2;
    }

    int diff(int val1, int val2){

        return val1-val2;
    }

    int mult(int val1, int val2){

        return val1*val2;
    }

    int div(int val1, int val2){

        return val1/val2;
    }

}
